const add = (anthony,kemi)=>{
    const mum = anthony + kemi
    return mum
}

const sub = (anthony,kemi)=>{
    const mum = kemi - anthony
    return mum
}

const multiply = (anthony,kemi)=>{
    const mum = anthony * kemi
    return mum
}

const division = (anthony,kemi)=>{
    const mum = anthony / kemi
    return mum
}

module.exports = {
    division,
    add,
    multiply,
    sub,
}